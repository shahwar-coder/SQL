"""
Summary: DDL, DML, and DQL in SQL

---------------------------------------------------------
1. DDL (Data Definition Language)
---------------------------------------------------------
• DDL is used to define or change the structure of the database.
• It deals with creating tables, altering them, or deleting them.
• Think of DDL as "setting up" the database.

Common DDL commands:
    - CREATE  → make a new table
    - ALTER   → change a table (add/remove columns)
    - DROP    → delete a table

---------------------------------------------------------
2. DML (Data Manipulation Language)
---------------------------------------------------------
• DML is used to work with the actual data inside the table.
• It helps you add new data, update existing data, or delete data.

Common DML commands:
    - INSERT  → add new rows
    - UPDATE  → modify rows
    - DELETE  → remove rows

---------------------------------------------------------
3. DQL (Data Query Language)
---------------------------------------------------------
• DQL is used to read or fetch data from the database.
• It does NOT change the data, only retrieves it.

Main DQL command:
    - SELECT  → get data from a table

---------------------------------------------------------
Quick Understanding:
• DDL = builds the table
• DML = fills or changes the data in the table
• DQL = reads data from the table
"""
